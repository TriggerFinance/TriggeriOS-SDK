import UIKit

extension UIColor
{
    
    // Grays and Blacks
    class func TriggerLightGray() -> UIColor {
        return UIColor(red: 238.0/255.0, green: 238.0/255.0, blue: 238.0/255.0, alpha: 1.0)
    }
    
    class func TriggerOffWhite() -> UIColor {
        return UIColor(red: 249.0 / 255.0, green: 249.0 / 255.0, blue: 249.0 / 255.0, alpha: 1.0)
    }
    
    class func TriggerWhite() -> UIColor {
        return UIColor.whiteColor()
    }
    
    class func TriggerWarmGray() -> UIColor {
        return UIColor(red: 127.0 / 255.0, green: 127.0 / 255.0, blue: 127.0 / 255.0, alpha: 1.0)
    }
    
    class func TriggerLightWarmGray() -> UIColor {
        return UIColor(red: 200.0 / 255.0, green: 200.0 / 255.0, blue: 200.0 / 255.0, alpha: 1.0)
    }
    
    class func TriggerBlack() -> UIColor {
        return UIColor(red: 46.0 / 255.0, green: 46.0 / 255.0, blue: 46.0 / 255.0, alpha: 1.0)
    }
    
    // BLUES
    class func TriggerLightBlue() -> UIColor {
        return UIColor(red: 137.0 / 255.0, green: 211.0 / 255.0, blue: 230.0 / 255.0, alpha: 1.0)
    }
    
    class func TriggerFadedBlue() -> UIColor {
        return UIColor(red: 95.0 / 255.0, green: 167.0 / 255.0, blue: 195.0 / 255.0, alpha: 1.0)
    }
    
    
    // GREENS
    class func TriggerLightGreen() -> UIColor {
        return UIColor(red: 155.0 / 255.0, green: 237.0 / 255.0, blue: 181.0 / 255.0, alpha: 1.0)
    }
    
    class func TriggerGreenTeal() -> UIColor {
        return UIColor(red: 75.0 / 255.0, green: 214.0 / 255.0, blue: 142.0 / 255.0, alpha: 1.0)
    }
    
    class func TriggerSofterGreen() -> UIColor {
        return UIColor(red: 92.0 / 255.0, green: 202.0 / 255.0, blue: 164.0 / 255.0, alpha: 1.0)
    }
    
    // ORANGES
    class func TriggerButterScotch() -> UIColor {
        return UIColor(red: 252.0 / 255.0, green: 172.0 / 255.0, blue: 59.0 / 255.0, alpha: 1.0)
    }
    
    class func TriggerApricot() -> UIColor {
        return UIColor(red: 252.0 / 255.0, green: 172.0 / 255.0, blue: 114.0 / 255.0, alpha: 1.0)
    }
    
    // REDS
    class func TriggerRosyPink() -> UIColor {
        return UIColor(red: 253.0 / 255.0, green: 181.0 / 255.0, blue: 178.0 / 255.0, alpha: 1.0)
    }
    
    class func TriggerDefaultRed() -> UIColor {
        return UIColor(red: 251.0 / 255.0, green: 120.0 / 255.0, blue: 114.0 / 255.0, alpha: 1.0)
    }
}
